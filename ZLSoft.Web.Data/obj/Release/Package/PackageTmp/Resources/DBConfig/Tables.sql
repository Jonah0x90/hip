  ValueStr Varchar2(4000);
  TMP VARCHAR(1000);
Begin
  BEGIN
    ValueStr := '';
    if strFlag_ ='拼音码' Then
      Begin
        WHILE LENGTH(strValueString_) > 0 LOOP
          BEGIN
            TMP := '';
            SELECT max(rtrim(ltrim(拼音码))) Into TMP FROM 系统_汉字库 WHERE 汉字=SUBSTR(strValueString_,1,1) AND 拼音码 LIKE '%';
             ValueStr := ValueStr|| TMP;
            strValueString_ := SUBSTR(strValueString_,2,length(strValueString_));
          End;
        END LOOP;
      End;
   ELSE
      Begin
        WHILE LENGTH(strValueString_) > 0 LOOP
        BEGIN
          SELECT max(五笔码)  Into TMP FROM 系统_汉字库 WHERE 汉字=SUBSTR(strValueString_,1,1) ;
          ValueStr := ValueStr || TMP;
          strValueString_ := SUBSTR(strValueString_,2,length(strValueString_));
        End;
       END LOOP;
      End;
